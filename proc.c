// Binary Search for upper bound
int bin_search(int * arr, int value, int low, int high)
{
   int mid = -1;
   while (low <= high)
   {
	mid = (low+high)/2;
        if (arr[mid] == value)
	{
	    return mid;
        }
	else if (arr[mid] < value)
	{
	    low = mid + 1;
	}
	else {
	    high = mid - 1;
	}
   }
   return mid;
}


